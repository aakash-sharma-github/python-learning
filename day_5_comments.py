# single line comment

print("This is single line comment.")

''' Multi line comment
    you can comment any text by using ctrl + / in vs code. 
    You can use triple single quote or triple double quote for multiline comment.
'''

print("This is multi line comment.")

# Escape sequence character.

# \n for new line.
print("hello\nworld")

# \" for " in arguments.
print("He is \"Good boy\"")

# \' for ' in arguments.
print("Today\'s News.")

# \t for tab in arguments. and many more.
print("This is a \ttab.")
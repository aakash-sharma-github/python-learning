# String slicing and operations
name = "Aakash"
# It will starts from A which is 0th index and print till 4th index which is s.
print(name[0:5])

# We can count strings with len() function in python.
str = "hello friends."
print(len(str))

# We can also index strings in negative.
# It will count string reverse wise from -1 and so on.
print(name[-4:-2]) # It will give output as 'ka'
